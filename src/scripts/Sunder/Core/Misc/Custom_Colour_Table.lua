function showColours(num)
    cecho("\n<green> Showing a list of available colours, with "..num.." columns: \n")
    local lineBreak = num
    local linecount = 0
    local padding = math.floor(90/num)
    
    for col, tab in pairs(color_table) do
        linecount = linecount + 1
        cecho(string.rep(" ", padding-#col).."<"..col..">"..col.."<NavajoWhite>,")
        if linecount >= lineBreak then
            echo("\n")
            linecount = 0
        end
    end
end